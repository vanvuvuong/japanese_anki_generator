def format_radical_for_card(radical_char: str) -> str:
    """Format radical info for Anki card display"""
    info = get_radical_info(radical_char)
    if not info:
        return ""

    importance = get_importance_label(info["frequency"], info["joyo_freq"])
    variants_str = ""
    if info["variants"]:
        variants_str = f" ({', '.join(info['variants'])})"

    return f"{info['symbol']}{variants_str} â€¢ {info['meaning_vn']} [{importance}]"


